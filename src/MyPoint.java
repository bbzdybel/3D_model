public class MyPoint {
    public double x, y, z;

    public MyPoint(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getZ(){
        return z;
    }

    public double getX(){
        return x;
    }

    public double getY(){
        return y;
    }
}
